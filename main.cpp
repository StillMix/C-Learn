// main.cpp - точка входа в наше приложение
#include <QApplication>  // Основной класс для Qt GUI приложений
#include "mainwindow/mainwindow.h"  // Включаем заголовочный файл нашего главного окна

int main(int argc, char *argv[]) {
    // Создаем экземпляр приложения Qt
    // argc и argv передаются из системы и содержат параметры запуска
    QApplication app(argc, argv);
    
    // Создаем экземпляр нашего главного окна
    MainWindow window;
    
    // Отображаем окно на экране
    window.show();
    
    // Запускаем цикл обработки событий приложения
    // app.exec() блокирует выполнение, пока окно не будет закрыто
    // Возвращаемое значение - код завершения программы
    return app.exec();
}